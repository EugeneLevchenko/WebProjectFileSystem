package com.eugene_levchenko.web.embeddedjetty.sax;

public class Logging {
    private String logging;

    public String getLogging() {
        return logging;
    }

    public void setLogging(String logging) {
        this.logging = logging;
    }

    @Override
    public String toString() {
        return "Logging{" +
                "logging=" + logging +'}';
    }
}
